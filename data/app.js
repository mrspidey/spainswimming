// ===============================
// GLOBAL STATE
// ===============================
let DATA = [];
let FILTERS = {
    gender: "all",
    province: "all"
};

// ===============================
// LOAD DATA
// ===============================
async function loadData() {
    const res = await fetch("data/frontend_results.csv");
    const text = await res.text();

    DATA = parseCSV(text).map(cleanRow);

    renderHome();
}

// ===============================
// CSV PARSER
// ===============================
function parseCSV(text) {
    const lines = text.split("\n");
    const headers = lines[0].split(",");

    return lines.slice(1).map(line => {
        const cols = line.split(",");
        let obj = {};
        headers.forEach((h, i) => obj[h] = cols[i]);
        return obj;
    });
}

// ===============================
// CLEAN ROW
// ===============================
function cleanRow(r) {
    return {
        swimmer: (r.Swimmer || "").trim(),
        club: (r.Club || "").trim(),
        event: normalizeEvent(r.Event),
        time: normalizeTime(r.Time),
        gender: (r.Gender || "unknown").toLowerCase(),
        meet: normalizeMeet(r.Meet),
        province: (r.Province || "").toLowerCase(),
        date: r.Date,
        rank: parseInt(r.Rank) || null
    };
}

// ===============================
// NORMALISERS
// ===============================
function normalizeEvent(e) {
    if (!e) return "";
    return e
        .replace(/m\s*/gi, "m ")
        .replace(/\s+/g, " ")
        .trim();
}

function normalizeMeet(m) {
    if (!m) return "";

    // Fix ugly autogenerated names
    return m
        .replace(/_/g, " ")
        .replace(/res\d+/gi, "")
        .replace(/liga/gi, "Liga")
        .replace(/jornada/gi, "Jornada")
        .replace(/\s+/g, " ")
        .trim();
}

// FIXES BAD TIMES (e.g. 3:00+)
function normalizeTime(t) {
    if (!t) return null;

    t = t.trim();

    // Already good
    if (t.includes(":")) return t;

    // If it's seconds like 34.63 → convert
    let num = parseFloat(t);
    if (!isNaN(num)) {
        let min = Math.floor(num / 60);
        let sec = (num % 60).toFixed(2).padStart(5, "0");
        return `${min}:${sec}`;
    }

    return null;
}

// Convert time to sortable number
function timeToSeconds(t) {
    if (!t || !t.includes(":")) return 9999;

    let [m, s] = t.split(":");
    return parseInt(m) * 60 + parseFloat(s);
}

// ===============================
// FILTERING
// ===============================
function applyFilters(rows) {
    return rows.filter(r => {
        if (FILTERS.gender !== "all" && r.gender !== FILTERS.gender) return false;
        if (FILTERS.province !== "all" && r.province !== FILTERS.province) return false;
        return true;
    });
}

// ===============================
// HOME VIEW
// ===============================
function renderHome() {
    const container = document.getElementById("app");

    const rows = applyFilters(DATA);

    const meets = groupBy(rows, "meet");

    let html = `
        <h1>Spain Swimming</h1>

        ${renderFilters()}

        <h2>Latest meets</h2>
    `;

    Object.entries(meets).forEach(([meet, rows]) => {
        const sample = rows[0];

        html += `
            <div class="card">
                <h3>${meet}</h3>
                <p>${sample.province} · ${sample.date}</p>
                <button onclick="openMeet('${meet}')">View results</button>
            </div>
        `;
    });

    container.innerHTML = html;
}

// ===============================
// FILTER UI
// ===============================
function renderFilters() {
    return `
        <div class="filters">
            <select onchange="setGender(this.value)">
                <option value="all">All</option>
                <option value="m">Male</option>
                <option value="f">Female</option>
            </select>
        </div>
    `;
}

function setGender(g) {
    FILTERS.gender = g;
    renderHome();
}

// ===============================
// MEET VIEW
// ===============================
function openMeet(meetName) {
    const rows = applyFilters(DATA).filter(r => r.meet === meetName);

    const events = groupBy(rows, "event");

    let html = `
        <button onclick="renderHome()">← Back</button>
        <h1>${meetName}</h1>
    `;

    Object.entries(events).forEach(([event, rows]) => {
        rows.sort((a, b) => timeToSeconds(a.time) - timeToSeconds(b.time));

        html += `
            <div class="event">
                <h2>${event}</h2>
                ${renderEventTable(rows)}
            </div>
        `;
    });

    document.getElementById("app").innerHTML = html;
}

